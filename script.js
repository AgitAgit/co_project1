// Essential Git Commands for Collaboration
// Here are the most important Git commands for effective collaboration on a project:

// Core Commands
// git init: Initializes a new Git repository in a directory.
// git clone: Creates a local copy of a remote repository.
// git add: Stages changes for commit.
// git commit: Records changes to the repository.
// git status: Shows the working tree status.
// git diff: Shows changes between commits, commit and working tree, etc.
// Branching and Merging
// git branch: Lists, creates, or deletes branches.
// git checkout: Switches between branches.
// git merge: Combines changes from one branch into another.
// git rebase: Reapplies commits on top of another base branch.
// Remote Repositories
// git remote: Manages remote repositories.
// git fetch: Downloads new data from a remote repository.
// git pull: Fetches and merges changes from a remote repository.
// git push: Uploads local repository content to a remote repository.
// Additional Useful Commands
// git log: Shows commit history.
// git reset: Resets the current HEAD to the specified state.
// git revert: Reverses the effect of a specific commit.
// git stash: Temporarily saves changes to be reapplied later.
// Understanding the Workflow
// While knowing these commands is crucial, understanding the typical Git workflow is equally important:

// Create a new branch for your feature or bug fix.
// Make changes to your code.
// Stage the changes using git add.
// Commit the changes with a clear message using git commit.
// Push your branch to the remote repository using git push.
// Create a pull request to merge your changes into the main branch.
// By mastering these commands and following a structured workflow, you can efficiently collaborate with your team and manage your project's codebase effectively.


